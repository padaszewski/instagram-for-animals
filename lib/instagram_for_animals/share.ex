defmodule InstagramForAnimals.Share do
  @moduledoc """
  The Share context.
  """

  import Ecto.Query, warn: false
  alias InstagramForAnimals.Repo

  alias InstagramForAnimals.Share.Photo

  @doc """
  Returns the list of photos.

  ## Examples

      iex> list_photos()
      [%Photo{}, ...]

  """
  def list_public_photos do
    IO.inspect "ALL PUBLIC PHOTOS"
    from(p in Photo, where: p.public == true)
    |> Repo.all()
    |> Repo.preload(:comments)
  end

  def list_public_photos_with_user_privates(user_id) do
    IO.inspect "PUBLIC AND PRIVATE PHOTOS OF USER"
    from(p in Photo, where: p.user_id == ^user_id)
    |> Repo.all()
    |> Repo.preload(:comments)
  end

  def list_users_public_photos(user_id) do
    IO.inspect "PUBLIC PHOTOS BY USER ID"
    from(p in Photo, where: p.public == true and p.user_id == ^user_id)
    |> Repo.all()
    |> Repo.preload(:comments)

  end

  @doc """
  Gets a single photo.

  Raises `Ecto.NoResultsError` if the Photo does not exist.

  ## Examples

      iex> get_photo!(123)
      %Photo{}

      iex> get_photo!(456)
      ** (Ecto.NoResultsError)

  """
  def get_photo!(id) do
    photo =
      Photo
      |> Repo.get!(id)
      |> Repo.preload(:comments)
  end

  @doc """
  Creates a photo.

  ## Examples

      iex> create_photo(%{field: value})
      {:ok, %Photo{}}

      iex> create_photo(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_photo(attrs \\ %{}) do
    %Photo{}
    |> Photo.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a photo.

  ## Examples

      iex> update_photo(photo, %{field: new_value})
      {:ok, %Photo{}}

      iex> update_photo(photo, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_photo(%Photo{} = photo, attrs) do
    photo
    |> Photo.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Photo.

  ## Examples

      iex> delete_photo(photo)
      {:ok, %Photo{}}

      iex> delete_photo(photo)
      {:error, %Ecto.Changeset{}}

  """
  def delete_photo(%Photo{} = photo) do
    Repo.delete(photo)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking photo changes.

  ## Examples

      iex> change_photo(photo)
      %Ecto.Changeset{source: %Photo{}}

  """
  def change_photo(%Photo{} = photo) do
    Photo.changeset(photo, %{})
  end

  alias InstagramForAnimals.Share.Comment

  @doc """
  Returns the list of comments.

  ## Examples

      iex> list_comments()
